#include "libft.h"

char				*ft_strcat(char *s1, const char *s2)
{
	unsigned int	i;
	unsigned int	j;

	j = ft_strlen(s1);
	i = 0;
	while (s2[i])
	{
		s1[i + j] = s2[i];
		i++;
	}
	s1[i + j] = '\0';
	return (s1);
}
