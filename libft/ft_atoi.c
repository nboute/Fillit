int	ft_atoi(const char *src)
{
	int	i;
	int	nb;
	int	neg;

	i = 0;
	neg = 1;
	while ((src[i] >= 9 && src[i] <= 13) || src[i] == 32)
		i++;
	if (src[i] == '-')
		neg = -1;
	if (src[i] == '+' || src[i] == '-')
		i++;
	nb = 0;
	while (src[i] >= '0' && src[i] <= '9')
	{
		nb = nb * 10 + src[i] - '0';
		i++;
	}
	return (nb * neg);
}
